type memDiffArgs struct {
	RunA int64 `json:"run_a" jsonschema:"First run id"`
	RunB int64 `json:"run_b" jsonschema:"Second run id"`
}

func memDiffTool() Tool {
	def := mcp.NewTool("dark_mem_diff",
		mcp.WithDescription("Diff two research runs by item title. Reports items that appeared only in run A, only in run B, and in both. Useful for tracking OSINT drift over time ('what changed about CVE-2024-3094 between this week and last?') or comparing two queries on the same target."),
		mcp.WithNumber("run_a", mcp.Required(), mcp.Description("First run id")),
		mcp.WithNumber("run_b", mcp.Required(), mcp.Description("Second run id")),
	)
	return Tool{
		Definition: def,
		Handler: func(ctx context.Context, req mcp.CallToolRequest) (*mcp.CallToolResult, error) {
			var args memDiffArgs
			if err := bindArgs(req, &args); err != nil {
				return nil, err
			}
			m := sharedMem()
			if m == nil {
				return nil, fmt.Errorf("mem store not configured")
			}
			a, err := m.ListResearchItems(ctx, args.RunA, "", 9999)
			if err != nil {
				return nil, err
			}
			b, err := m.ListResearchItems(ctx, args.RunB, "", 9999)
			if err != nil {
				return nil, err
			}
			onlyA, onlyB, shared := diffItemsByTitle(a, b)
			return jsonResult(map[string]any{
				"run_a_id":       args.RunA,
				"run_b_id":       args.RunB,
				"count_only_a":   len(onlyA),
				"count_only_b":   len(onlyB),
				"count_shared":   len(shared),
				"only_in_run_a":  onlyA,
				"only_in_run_b":  onlyB,
				"shared":         shared,
			}), nil
		},
	}
}

// diffItemsByTitle partitions items by title (case-insensitive). Returns
// (onlyInA, onlyInB, shared). Used by dark_mem_diff.
func diffItemsByTitle(a, b []mem.Item) (onlyA, onlyB, shared []mem.Item) {
	aMap := map[string]mem.Item{}
	for _, it := range a {
		aMap[strings.ToLower(strings.TrimSpace(it.Title))] = it
	}
	bMap := map[string]mem.Item{}
	for _, it := range b {
		bMap[strings.ToLower(strings.TrimSpace(it.Title))] = it
	}
	for k, it := range aMap {
		if _, ok := bMap[k]; ok {
			shared = append(shared, it)
		} else {
			onlyA = append(onlyA, it)
		}
	}
	for k, it := range bMap {
		if _, ok := aMap[k]; ok {
			continue // already in shared
		}
		onlyB = append(onlyB, it)
	}
	return
}

